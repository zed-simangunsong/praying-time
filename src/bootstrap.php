/*
|--------------------------------------------------------------------------
| Start session.
|--------------------------------------------------------------------------
|
*/
session_start();

/*
|--------------------------------------------------------------------------
| Form token. Required when post request been made.
|--------------------------------------------------------------------------
|
*/
if (!isset($_SESSION['token']) || empty($_SESSION['token'])) {
    $_SESSION['token'] = bin2hex(random_bytes(32));
}

/*
|--------------------------------------------------------------------------
| Last refresh date. Used to refresh the browser in
| daily basis, in case it did not need to refresh,
| manually such in display case.
|--------------------------------------------------------------------------
*/
$_SESSION['last_refresh'] = date('Y-m-d');

/*
|--------------------------------------------------------------------------
| Root directory.
|--------------------------------------------------------------------------
|
*/
define('ROOT_PATH', __DIR__ . '/');

/*
|--------------------------------------------------------------------------
| Public directory.
|--------------------------------------------------------------------------
|
*/
define('PUBLIC_PATH', ROOT_PATH . 'public/');

/*
|----------------------- ---------------------------------------------------
| App directory.
|--------------------------------------------------------------------------
|
*/
define('APP_PATH', ROOT_PATH . '../src/app/');

/*
|--------------------------------------------------------------------------
| System directory.
|--------------------------------------------------------------------------
|
*/
define('VIEW_PATH', APP_PATH . 'Views/');

/*
|--------------------------------------------------------------------------
| Cache directory.
|--------------------------------------------------------------------------
|
*/
define('CACHE_PATH', ROOT_PATH . '../cache/');

/*
|--------------------------------------------------------------------------
| Cache directory.
|--------------------------------------------------------------------------
|
*/
define('CACHE_VIEW', CACHE_PATH . 'Views/');

/*
|--------------------------------------------------------------------------
| Composer autoloader.
|--------------------------------------------------------------------------
*/
include '../vendor/autoload.php';

/*
|--------------------------------------------------------------------------
| Load environment configurations.
|--------------------------------------------------------------------------
*/
$env = Dotenv\Dotenv::createImmutable(__DIR__ . DIRECTORY_SEPARATOR . '..');
$env->load();

/*
|--------------------------------------------------------------------------
| App base url.
|--------------------------------------------------------------------------
|
*/
define('BASE_URL', env('BASE_URL', '/'));